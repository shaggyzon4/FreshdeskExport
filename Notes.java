import com.csvreader.CsvWriter;

public class Notes
{
    private Note note;

    public Note getNote ()
    {
        return note;
    }

    public void setNote (Note note)
    {
        this.note = note;
    }

    public void toCSV(String display_id, String id)
    {
    	//export notes to CSV
    	this.note.toCSV(display_id, id);
    }

    @Override
    public String toString()
    {
        return "ClassPojo [note = "+note+"]";
    }
}